## Loops

# While Loop
i = 1
while i < 6:
  print(i)
  i += 1
## Break Statement
i = 1
while i < 6:
  print(i)
  if i == 3:
    break
  i += 1 


# For Loop
fruits = ["apple", "banana", "cherry"]
for x in fruits:
  print(x)

for x in "banana":
  print(x)

fruits = ["apple", "banana", "cherry"]
for x in fruits:
  print(x)
  if x == "banana":
    break

# range
for x in range(6):
  print(x)
for x in range(2, 6):
  print(x)
for x in range(2, 14, 3):
  print(x)








